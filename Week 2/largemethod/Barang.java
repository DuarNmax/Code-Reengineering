package largemethod;

public class Barang {
	
	private int harga, berat;

	public Barang(int harga, int berat) {
		super();
		this.harga = harga;
		this.berat = berat;
	}

	public int getHarga() {
		return harga;
	}

	public void setHarga(int harga) {
		this.harga = harga;
	}

	public int getBerat() {
		return berat;
	}

	public void setBerat(int berat) {
		this.berat = berat;
	}
	

}
