package largeclass;

public class Kampus {
	private String nama, lokasi, 
			strata, tipePerkuliahan;

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public String getLokasi() {
		return lokasi;
	}

	public void setLokasi(String lokasi) {
		this.lokasi = lokasi;
	}

	public String getStrata() {
		return strata;
	}

	public void setStrata(String strata) {
		this.strata = strata;
	}

	public String getTipePerkuliahan() {
		return tipePerkuliahan;
	}

	public void setTipePerkuliahan(String tipePerkuliahan) {
		this.tipePerkuliahan = tipePerkuliahan;
	}
	
	
}
